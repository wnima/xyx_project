package data.bean;

public class Refine {

	private long lordId;
	private int refineId;
	private int level;
	public long getLordId() {
		return lordId;
	}
	public void setLordId(long lordId) {
		this.lordId = lordId;
	}
	public int getRefineId() {
		return refineId;
	}
	public void setRefineId(int refineId) {
		this.refineId = refineId;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	
}
