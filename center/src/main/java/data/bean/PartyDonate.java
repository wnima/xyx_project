package data.bean;

public class PartyDonate {

	private int stone;
	private int iron;
	private int silicon;
	private int copper;
	private int oil;
	private int gold;

	public int getStone() {
		return stone;
	}

	public void setStone(int stone) {
		this.stone = stone;
	}

	public int getIron() {
		return iron;
	}

	public void setIron(int iron) {
		this.iron = iron;
	}

	public int getSilicon() {
		return silicon;
	}

	public void setSilicon(int silicon) {
		this.silicon = silicon;
	}

	public int getCopper() {
		return copper;
	}

	public void setCopper(int copper) {
		this.copper = copper;
	}

	public int getOil() {
		return oil;
	}

	public void setOil(int oil) {
		this.oil = oil;
	}

	public int getGold() {
		return gold;
	}

	public void setGold(int gold) {
		this.gold = gold;
	}

}
